/**
 * add - adds int
 * @a: 1st num
 * @b: 2nd num
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts 2 ints
 * @a: 1st num
 * @b: 2nd num
 * Return: sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies
 * @a: 1st num
 * @b: 2nd num
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides
 * @a: 1st num
 * @b: 2nd num
 * Return: dividend
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: 1st num
 * @b: 2nd num
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
